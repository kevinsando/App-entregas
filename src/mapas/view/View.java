    public void init() {
        runner = new Thread() {
            @Override
            public void run() {
                while (runner == Thread.currentThread() && runCheck.isSelected()) {
                    repaint();
                    Iterator<Carrier> iteratorC = carriers.getIterator();
                    while (iteratorC.hasNext()) {
                        graph.updateCarrier(iteratorC.getNext());
                    }

                    try {
                        Thread.sleep(50);
                    } catch (InterruptedException ex) {
                    }
                }
            }

        };
        runner.start();
    }

